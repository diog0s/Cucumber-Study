package br.cin.ufpe;

public class Calculadora {
    public int somar(int numA, int numB) {
        return numA + numB;
    }

    public int subtrair(int numA, int numB) {
        return numA - numB;
    }

    public int dividir(int numA, int numB) {
        return numA / numB;
    }

    public int multiplicar(int numA, int numB) {
        return numA * numB;
    }
}
